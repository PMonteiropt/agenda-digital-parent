package com.everis.academia.agenda.digital.entity;

public class Cidade {

	private Short codigo;
	private String nome;
	

	
 	public Cidade() {
		super();
	}	
	
	
	public Short getCodigo() {
		return codigo;
	}
	public void setCodigo(Short codigo) {
		this.codigo = codigo;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	
	
	

}
